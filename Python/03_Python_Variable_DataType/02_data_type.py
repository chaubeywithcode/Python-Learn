# python supports several built-in data type:
#      Integers (Int): Whole number (e.g. , 10, -5)
#      Floats (float): Decimal number (e.g, 3.14, -0.0004)
#      String (str):   text data enclosed in quotes (e.g., "Helo", 'python')
#      Booleans (bool): Represents True or False.
#      List: ordered mutable collection (e.g., [1, 2, 3]). 
#      Tuples: ordered immutable collection (e.g., (1,2,3)).
#      Sets: Unordered collection of unique element (e.g., {1, 2, 3}).
#      Dictionaries key-value pairs (e.g., {"name" : "Alice", "age" : 35})

age = 3
print(age)
print(type(age))

cgpa = 8.2
print(cgpa)
print(type(cgpa))

name = "Anurag"
print(name)
print(type(name))

is_completed = True
print(is_completed)
print(type(is_completed))